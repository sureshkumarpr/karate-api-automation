try {
    # Start Python HTTP server if available, otherwise use PowerShell listener
    $pythonCmd = Get-Command python -ErrorAction SilentlyContinue
    if ($pythonCmd) {
        Write-Host "Starting Python HTTP server..." -ForegroundColor Green
        Set-Location $reportPath
        python -m http.server $port
    } else {
        Write-Host "Starting PowerShell HTTP listener..." -ForegroundColor Green
        $listener = New-Object System.Net.HttpListener
        $listener.Prefixes.Add("http://localhost:$port/")
        $listener.Start()

        Write-Host "Web server started!" -ForegroundColor Green
        Write-Host "Local URL: http://localhost:$port/$reportFile" -ForegroundColor Cyan
        Write-Host "Network URL: http://$((Get-NetIPAddress -AddressFamily IPv4 -InterfaceAlias 'Ethernet*').IPAddress[0]):$port/$reportFile" -ForegroundColor Yellow
        Write-Host "Press Ctrl+C to stop" -ForegroundColor White

        while ($listener.IsListening) {
            $context = $listener.GetContext()
            $request = $context.Request
            $response = $context.Response

            $requestedPath = $request.Url.LocalPath
            if ($requestedPath -eq "/" -or $requestedPath -eq "") {
                $requestedPath = "/$reportFile"
            }

            $fileName = $requestedPath.TrimStart('/')
            $filePath = Join-Path $reportPath $fileName

            if (Test-Path $filePath -PathType Leaf) {
                $content = [System.IO.File]::ReadAllBytes($filePath)
                $response.ContentLength64 = $content.Length
                $response.ContentType = "text/html"
                $response.OutputStream.Write($content, 0, $content.Length)
            } else {
                $response.StatusCode = 404
            }

            $response.Close()
        }
    }
} catch {
    Write-Host "Error: $_" -ForegroundColor Red
}
